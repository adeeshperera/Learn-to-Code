// Define a public class named HelloWorld
public class HelloWorld {

    // The main method is the entry point of any Java application
    // It must be declared as 'public', 'static', and 'void'
    // 'public': The method is accessible from outside the class
    // 'static': The method belongs to the class rather than an instance of the class
    // 'void': The method does not return any value
    // 'String[] args': The parameter 'args' is an array of Strings used to pass command-line arguments
    public static void main(String[] args) {

        // Print "Hello, World!" to the console
        System.out.println("Hello, World!");
    }
}
